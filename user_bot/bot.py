import os, asyncio, logging, pathlib
from dotenv import load_dotenv

from aiogram import Bot, Dispatcher
from data.event_logger import EventLogger           # ‚Üê NEW
from precache_videos import precache_videos, _load_cache
from utils.reminders import reminders_scheduler
from handlers.user_handlers import router as user_router

# ‚îÄ‚îÄ .env ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
ROOT_DIR = pathlib.Path(__file__).resolve().parent.parent
load_dotenv(dotenv_path=ROOT_DIR / ".env")
USER_BOT_TOKEN = os.getenv("USER_BOT_TOKEN")
admin_ids_raw = os.getenv("ADMIN_IDS") or ""
ADMIN_ID = int(admin_ids_raw.split(",")[0].strip() or "0")

bot = Bot(token=USER_BOT_TOKEN)
dp  = Dispatcher()
VIDEO_ID_CACHE: dict = {}

# ‚îÄ‚îÄ‚îÄ MIDDLEWARE: —Å–±–æ—Ä –∫–ª–∏–∫–æ–≤ ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
evlog = EventLogger()          # —ç–∫–∑–µ–º–ø–ª—è—Ä; —Å–æ–µ–¥–∏–Ω–∏—Ç—Å—è –ø—Ä–∏ startup
dp.update.middleware(evlog)    # —Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä—É–µ–º –≤ –¥–∏—Å–ø–µ—Ç—á–µ—Ä–µ

# ‚îÄ‚îÄ‚îÄ STARTUP HOOK ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
async def on_startup(dispatcher: Dispatcher) -> None:
    global VIDEO_ID_CACHE
    if ADMIN_ID:
        VIDEO_ID_CACHE = await precache_videos(bot, ADMIN_ID)
        print("Video cache ready:", VIDEO_ID_CACHE)
    else:
        logging.warning("ADMIN_ID not set; skipping video precache")

    reminders_task = asyncio.create_task(reminders_scheduler(bot))   # —Ñ–æ–Ω–æ–≤—ã–π –ø–ª–∞–Ω–∏—Ä–æ–≤—â–∏–∫
    reminders_task.add_done_callback(
        lambda task: logging.error("reminders_scheduler stopped: %s", task.exception())
        if task.exception() else None
    )
    # –æ—Ç–∫—Ä—ã–≤–∞–µ–º SQLite –¥–ª—è middleware
    await evlog.startup()

# ‚îÄ‚îÄ‚îÄ SHUTDOWN HOOK ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
async def on_shutdown(dispatcher: Dispatcher) -> None:
    await evlog.shutdown()

# ‚îÄ‚îÄ‚îÄ MAIN ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
def main() -> None:
    logging.basicConfig(
        level=logging.DEBUG,
        format="%(asctime)s [%(levelname)s] %(name)s: %(message)s",
        datefmt="%Y-%m-%d %H:%M:%S",
    )

    logging.info("üöÄ –ó–∞–ø—É—Å–∫ –±–æ—Ç–∞...")

    dp.startup.register(on_startup)
    dp.shutdown.register(on_shutdown)
    dp.include_router(user_router)

    dp.run_polling(bot)            # Dispatcher —Å–∞–º –∑–∞–ø—É—Å–∫–∞–µ—Ç —Ü–∏–∫–ª —Å–æ–±—ã—Ç–∏–π

if __name__ == "__main__":
    main()

